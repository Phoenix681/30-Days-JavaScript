// Arithmetic Operator
var a = 5+4;
console.log(a);
var b = 5-4;
console.log(b);
var c = 5/4;
console.log(c);
var d = 5*4;
console.log(d);
var e = 5%4;
console.log(e);

// Assignment Operator

var f = 2;
f+=5;
console.log(f);

var g = 3;
g-=8;
console.log(g);

// Comparison Operator

console.log(a<b);
console.log(a>=g);
console.log(b===e)
console.log(b==='1')
console.log(b=='1')

// Logical Operator

var p = 5>8;
var q = 6<=7;
var r = 1==1;
console.log(p && q);
console.log(q || p);
console.log(!r)

// Ternary Operator

var x = -9;
var t = x>=0 ? 'Positive':'Negative';
console.log(t)