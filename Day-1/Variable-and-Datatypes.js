// Variable Declaration

var n = 15;
console.log(n);

let s = "Hello World";
console.log(s);

// Constant Declaration

const f = true;
console.log(f);

// var vs let vs const 
// var can be updated and redeclared  
// let can be updated but not redeclared
// const can neither be updated nor redeclared

// Data Types

var a = 6;
